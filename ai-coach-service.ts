      const response = await ai.models.generateContent({
        model: "gemini-3.5-flash",
        contents: userPrompt,
        config: {
          systemInstruction: "You are the GoalTracker coach helper. Write extremely brief and catchy study notification content.",
          temperature: 0.85,
        }
      });
      return response.text?.trim() || `Time to work on ${taskOrHabitName}! Keep up your amazing study momentum.`;
