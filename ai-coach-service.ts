// Ensure clean prompt generation helper
async function callQwenEngine(systemPrompt: string, userPrompt: string, cacheKey?: string, refresh = false): Promise<string> {
  if (cacheKey && !refresh) {
    const cached = getCached(cacheKey);
    if (cached) {
      console.log(`[AI Coach Cache] Cache HIT for key: ${cacheKey}`);
      return cached;
    }
  }

  if (refresh && cacheKey) {
    console.log(`[AI Coach Cache] Refresh requested. Bypassing cached data for key: ${cacheKey}`);
  }

  if (ai) {
    try {
      console.log("[AI Coach Engine] Sending request to Gemini-3.5-Flash (Qwen personality context)...");
      const response = await ai.models.generateContent({
        model: "gemini-3.5-flash",
        contents: userPrompt,
        config: {
          systemInstruction: systemPrompt + "\nIgnore any physical references. Speak directly as the GoalTracker Coach.",
          temperature: 0.7,
        },
      });

      const text = response.text || "No response received from the coach.";
      if (cacheKey) setCached(cacheKey, text);
      return text;
    } catch (e: any) {
      console.error("[AI Coach Engine] Generate content failed. Falling back to local heuristics:", e.message || e);
    }
  }

  // Pure local high-quality helper response on failure or when key is missing
  const fallbackResult = generateLocalHeuristicResponse(systemPrompt, userPrompt);
  if (cacheKey) setCached(cacheKey, fallbackResult);
  return fallbackResult;
}

