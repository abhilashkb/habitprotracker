// 16. PERFORMANCE-TUNED NATURAL LANGUAGE SEARCH
export interface SearchMatches {
  goals: Goal[];
  tasks: GoalTask[];
  skills: Skill[];
  courses: Course[];
  projects: Project[];
  dailies: DailyTask[];
  insightMatches: string[];
}

export function performNaturalSearch(userId: string, query: string, data: any, insights: InsightsData): SearchMatches {
  const q = query.toLowerCase().trim();
  const res: SearchMatches = {
    goals: [],
    tasks: [],
    skills: [],
    courses: [],
    projects: [],
    dailies: [],
    insightMatches: []
  };

  const userGoals = data.goals.filter((g: any) => g.userId === userId);
  const userTasks = data.tasks.filter((t: any) => t.userId === userId);
  const userSkills = data.skills.filter((s: any) => s.userId === userId);
  const userCourses = data.courses.filter((c: any) => c.userId === userId);
  const userProjects = data.projects.filter((p: any) => p.userId === userId);
  const userDailies = data.dailies.filter((d: any) => d.userId === userId);

  // Intent: Overdue items
  if (q.includes("overdue") || q.includes("behind schedule") || q.includes("missed")) {
    const todayStr = getTodayDateString();
    res.tasks = userTasks.filter((t: any) => !t.completed && t.dueDate && t.dueDate < todayStr);
    res.dailies = userDailies.filter((d: any) => d.status === "missed");
    res.insightMatches.push("Overdue checklist query detected. Listed tasks whose due dates have slipped past today.");
  }
  
  // Intent: Skills below certain confidence limit
  else if (q.includes("confidence") || q.includes("weak skills") || q.includes("level 3") || q.includes("below")) {
    res.skills = userSkills.filter((s: any) => s.confidenceLevel < 3);
    res.insightMatches.push("Confidence filter active. Showing all skill items where your self-declared index is minor (< 3).");
  }

  // Intent: Inactive / Stall courses
  else if (q.includes("not worked on") || q.includes("recent") || q.includes("inactive") || q.includes("stalled")) {
    res.courses = userCourses.filter((c: any) => c.pausedStatus || c.progressPercentage < 50);
    res.projects = userProjects.filter((p: any) => p.status === "Planning" || p.progressPercentage < 30);
    res.insightMatches.push("Stall risk identified. Flagging courses and projects whose progress index is relatively low (< 50%).");
  }

  // General Text Scanning Match Fallback (maps onto all items)
  else {
    res.goals = userGoals.filter((g: any) => g.title.toLowerCase().includes(q) || g.description.toLowerCase().includes(q));
    res.tasks = userTasks.filter((t: any) => t.title.toLowerCase().includes(q) || (t.description && t.description.toLowerCase().includes(q)));
    res.skills = userSkills.filter((s: any) => s.skillName.toLowerCase().includes(q));
    res.courses = userCourses.filter((c: any) => c.courseName.toLowerCase().includes(q));
    res.projects = userProjects.filter((p: any) => p.title.toLowerCase().includes(q) || (p.description && p.description.toLowerCase().includes(q)));
    res.dailies = userDailies.filter((d: any) => d.title.toLowerCase().includes(q));
  }

  return res;
}

